int	ft_sqrt(int nb)
{
	long	a;
	long	b;

	a = nb;
	if (a <= 0)
		return (0);
	if (a == 1)
		return (1);
	b = 2;
	if (a >= 2)
	{
		while (b * b <= a)
		{
			if (b * b == a)
				return (b);
			b++;
		}
	}
	return (0);
}
